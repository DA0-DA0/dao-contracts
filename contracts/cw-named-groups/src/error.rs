use cosmwasm_std::StdError;
use thiserror::Error;

#[derive(Error, Debug, PartialEq)]
pub enum ContractError {
    #[error("{0}")]
    Std(#[from] StdError),

    #[error("Unauthorized")]
    Unauthorized {},

    #[error("Invalid group: {0}")]
    InvalidGroup(String),

    #[error("Invalid address: {0}")]
    InvalidAddress(String),
}
